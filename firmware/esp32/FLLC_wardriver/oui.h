/*
 * oui_lookup — Binary-ish linear scan (fast enough for <400 entries on ESP32)
 * Returns vendor string or NULL if not found.
 */
static const char *oui_lookup(const uint8_t *mac) {
  for (int i = 0; i < OUI_DB_SIZE; i++) {
    if (mac[0] == OUI_DB[i].oui[0] &&
        mac[1] == OUI_DB[i].oui[1] &&
        mac[2] == OUI_DB[i].oui[2]) {
      return OUI_DB[i].vendor;
    }
  }
  return NULL;
}

